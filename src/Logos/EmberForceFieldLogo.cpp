void BuildMesh_TXT_FieldForce( Mesh& outMesh ) {
    int vertCount = sizeof( verts ) / sizeof( verts[0] ) / 3;
    int faceCount = sizeof( faces ) / sizeof( faces[0] ) / 5;

    BuildMesh_EmberIcon( outMesh );

    int vertBase = outMesh.getNumVerts();
    int faceBase = outMesh.getNumFaces();

    outMesh.setNumVerts( vertBase + vertCount, TRUE );
    outMesh.setNumFaces( faceBase + faceCount, TRUE );

    for( int i = 0; i < vertCount; ++i )
        outMesh.setVert( vertBase + i, &verts[i * 3] );

    for( int i = 0; i < faceCount; ++i ) {
        memcpy( &outMesh.faces[faceBase + i], &faces[i * 5], sizeof( Face ) );

        outMesh.faces[faceBase + i].v[0] += vertBase;
        outMesh.faces[faceBase + i].v[1] += vertBase;
        outMesh.faces[faceBase + i].v[2] += vertBase;
    }

    outMesh.InvalidateGeomCache();
    outMesh.InvalidateTopologyCache();
    outMesh.buildBoundingBox();
}

